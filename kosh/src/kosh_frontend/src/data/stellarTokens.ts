export const popularTokens = stellarTokens.slice(0, 10);

// Find token by symbol
export const findTokenBySymbol = (symbol: string): StellarToken | undefined => {
  return stellarTokens.find(token => token.symbol.toLowerCase() === symbol.toLowerCase());
};

// Find token by issuer
export const findTokenByIssuer = (issuer: string): StellarToken | undefined => {
  return stellarTokens.find(token => token.issuer === issuer);
};

// Find token by symbol and issuer
export const findToken = (symbol: string, issuer: string): StellarToken | undefined => {
  return stellarTokens.find(token => 
    token.symbol.toLowerCase() === symbol.toLowerCase() && 
    token.issuer === issuer
  );
};
